const options = document.querySelectorAll(".option");
const cardContainer = document.getElementById("card-container");

function updateCards(category) {
  cardContainer.innerHTML = ""; // Clear existing cards
  const cards = categories[category];
  cards.forEach((card) => {
    const cardElement = document.createElement("div");
    cardElement.classList.add("card");
    cardElement.innerHTML = `
            <iframe title="Human Internal Organs Anatomy" frameborder="0" allowfullscreen mozallowfullscreen="true" webkitallowfullscreen="true" allow="autoplay; fullscreen; xr-spatial-tracking" xr-spatial-tracking execution-while-out-of-viewport execution-while-not-rendered web-share src="${card.image}"> </iframe> 
            <h3>${card.title}</h3>
            
          `;
    cardContainer.appendChild(cardElement);
  });
}

options.forEach((option) => {
  option.addEventListener("click", () => {
    document.querySelector(".option.active").classList.remove("active");
    option.classList.add("active");
    updateCards(option.getAttribute("data-category"));
  });
});

